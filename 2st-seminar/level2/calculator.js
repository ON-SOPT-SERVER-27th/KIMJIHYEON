const add = (a,b) => {
    let result = a + b
    return result
}

const substract = (a,b) => {
    let result = a - b
    return result
}

const multiply = (a,b) => {
    let result = a * b
    return result
}

const divide = (a,b) => {
    let result = a / b
    return result
}

module.exports = {
    add,
    substract,
    multiply,
    divide
}